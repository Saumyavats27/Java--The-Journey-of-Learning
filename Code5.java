// Code Block 
//Java allows two or more statement to be grouprd into block of code.


public class Code5 {
    public static void main(String[] args) {
        int x, y;
        y = 20;
        // target of this loop is a block
        for (x = 0; x < 10; x++) {
            System.out.println("This is x:" + x);
            System.out.println("This is y:" + y);
        }
    }

}
